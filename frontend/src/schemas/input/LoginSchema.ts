import { z } from "zod";

export const loginSchema = z.object({
  email: z
    .string()
    .min(1, "Email wajib diisi")
    .email("Format email tidak valid"),

  password: z
    .string()
    .min(1, "Password wajib diisi")
    .min(3, "Password minimal 3 karakter"),
});

export type LoginSchema = z.infer<typeof loginSchema>;
